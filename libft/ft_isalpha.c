// This function ft_isalpha checks if the character is alphabetic (a-z && A-Z).
// No locale.
// Return: 1 if yes 0 if not as int.

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
